package com.mycompany.pojo;

public class TinDetail {

	private String searialNO;
	private String oldRcNo;
	private String tinNumber;
	private String dealerName;
	private String status;
	private TinDetailExplosion tinDetailExplosion;

	public String getSearialNO() {
		return searialNO;
	}
	public void setSearialNO(String searialNO) {
		this.searialNO = searialNO;
	}
	public String getOldRcNo() {
		return oldRcNo;
	}
	public void setOldRcNo(String oldRcNo) {
		this.oldRcNo = oldRcNo;
	}
	public String getTinNumber() {
		return tinNumber;
	}
	public void setTinNumber(String tinNumber) {
		this.tinNumber = tinNumber;
	}
	public String getDealerName() {
		return dealerName;
	}
	public void setDealerName(String dealerName) {
		this.dealerName = dealerName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public TinDetailExplosion getTinDetailExplosion() {
		return tinDetailExplosion;
	}
	public void setTinDetailExplosion(TinDetailExplosion tinDetailExplosion) {
		this.tinDetailExplosion = tinDetailExplosion;
	}

}
